function setLang(lang) {
  localStorage.setItem('riosp_lang', lang);
  const t = (lang === 'pt') ? null : TRANSLATIONS[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (!el.dataset.i18nOrig) el.dataset.i18nOrig = el.textContent;
    el.textContent = (t && t[key] !== undefined) ? t[key] : el.dataset.i18nOrig;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (!el.dataset.i18nOrig) el.dataset.i18nOrig = el.innerHTML;
    el.innerHTML = (t && t[key] !== undefined) ? t[key] : el.dataset.i18nOrig;
  });

  // Mark active language in dropdown
  document.querySelectorAll('.idioma-option').forEach(opt => {
    opt.style.fontWeight = (opt.dataset.lang === lang) ? '900' : '';
  });

  // Close dropdown
  document.querySelectorAll('.idioma-wrap').forEach(w => w.classList.remove('open'));
}

function initLang() {
  const saved = localStorage.getItem('riosp_lang') || 'pt';
  if (saved !== 'pt') setLang(saved);
  document.querySelectorAll('.idioma-option').forEach(opt => {
    if (opt.dataset.lang === saved) opt.style.fontWeight = '900';
  });
}
